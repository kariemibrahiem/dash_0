if (!function_exists('trns')) {
    function trns($key)
    {
        $path = resource_path("lang/en/file.php");

        // Ensure the language file exists
        if (!File::exists($path)) {
            File::put($path, "<?php\n\nreturn [];\n");
        }
        $translations = include $path;
        // Convert key to human-readable format
        $value = ucwords(str_replace('_', ' ', $key));

        if (!array_key_exists($key, $translations)) {
            $translations[$key] = $value;

            // Save the translations back to the file
            $exported = var_export($translations, true);
            File::put($path, "<?php\n\nreturn {$exported};\n");
            return trans('file.' . $key);
        } else {
            return trans('file.' . $key);
        }
    }
}

if (!function_exists('routeActive')) {
    function routeActive($routeName, $class = 'active')
    {
        $currentRoute = Route::currentRouteName();

        // Exact match
        if ($currentRoute === $routeName) {
            return $class;
        }

        // Wildcard match (if routeName ends with *)
        if (str_ends_with($routeName, '*')) {
            $baseRoute = rtrim($routeName, '*');
            if (str_starts_with($currentRoute, $baseRoute)) {
                return $class;
            }
        }

        return '';
    }
}

if (!function_exists('arrRouteActive')) {
    function arrRouteActive($routesName, $class = 'is-expanded')
    {
        $currentRoute = Route::currentRouteName();

        foreach ($routesName as $route) {
            // Exact match
            if ($currentRoute === $route) {
                return $class;
            }

            // Wildcard match
            if (str_ends_with($route, '*')) {
                $baseRoute = rtrim($route, '*');
                if (str_starts_with($currentRoute, $baseRoute)) {
                    return $class;
                }
            }
        }

        return '';
    }
}


if (!function_exists('getTranslationsFromData')) {

    function getTranslationsFromData(array $data, string $fieldPrefix = 'name_', array $existingTranslations = []): array
    {
        $supportedLangs = config('app.locales');
        $translations = [];

        foreach ($supportedLangs as $langCode => $languageName) {
            if (isset($data[$fieldPrefix . $langCode])) {
                $translations[$langCode] = $data[$fieldPrefix . $langCode];
            } else {
                $translations[$langCode] = $existingTranslations[$langCode] ?? '';
            }
        }

        return $translations;
    }
}
