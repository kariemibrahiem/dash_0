function highlightWordsByPosition($text, $positions)
{
    $words = explode(' ', $text);

    foreach ($positions as $position) {

        if (isset($words[$position - 1])) {
            $words[$position - 1] = '<span class="highlight">' . $words[$position - 1] . '</span>';
        }
    }

    return implode(' ', $words);
}


